export const findScoreById = (id: number): Score | undefined => {
    return mockScores.find(sc => sc.id === id)
}

export const findScoresByStudent = (studentId: number): Score[] => {
    return mockScores.filter(sc => sc.studentId === studentId)
}

export const searchScores = (
    startScore: number,
    endScore: number,
    startCreateDate: string,
    endCreateDate: string
): Score[] => {
    return mockScores.filter(sc => {
        if (sc.score < startScore) return false;
        if (sc.score > endScore) return false;
        if (sc.createDate < startCreateDate) return false;
        if (sc.createDate > endCreateDate) return false;
        return true;
    });
};

export const deleteScoresByStudent = (studentIdArray: number[]): Score[] => {
    if (studentIdArray.length === 0) return mockScores;
    const idSet = new Set(studentIdArray);
    return mockScores.filter(sc => !idSet.has(sc.studentId));
}